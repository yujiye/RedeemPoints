#pragma mark - md5
+(NSString *)md5FromHash:(NSString *)str {
    const char *cStr = [str UTF8String];
    unsigned char result[16];
#pragma clang diagnostic push
#pragma clang diagnostic ignored "-Wconversion"
    CC_MD5( cStr, strlen(cStr), result );
#pragma clang diagnostic pop

    
    
    NSMutableString *md5Result = [NSMutableString stringWithCapacity:CC_MD5_DIGEST_LENGTH];
    for (NSInteger i=0; i<CC_MD5_DIGEST_LENGTH; i++) {
        [md5Result appendFormat:@"%02X", result[i]];
    }
    return md5Result;
}
+(NSString *)md5FromData:(NSData *)data
{
    if( !data || ![data length] )
        return nil;
    unsigned char result[CC_MD5_DIGEST_LENGTH];
#pragma clang diagnostic push
#pragma clang diagnostic ignored "-Wconversion"
    CC_MD5([data bytes], [data length], result);
#pragma clang diagnostic pop
    return [[NSString stringWithFormat:
             @"%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X%02X",
             result[0], result[1], result[2], result[3],
             result[4], result[5], result[6], result[7],
             result[8], result[9], result[10], result[11],
             result[12], result[13], result[14], result[15]
             ] uppercaseString];
}
+(NSString *)md5FromFile:(NSString *)file
{
    if( nil == file )
        return nil;
    NSData * data = [NSData dataWithContentsOfFile:file];
    return [self md5FromData:data];
}


